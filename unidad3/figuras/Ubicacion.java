package unidad3.figuras;

/**
 *
 * @author paveg
 */
public class Ubicacion {
    private int x;
    private int y;

    public Ubicacion() {}
    
    public Ubicacion(int x, int y) {
        this.x = x;
        this.y = y;
    }

    
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
}
